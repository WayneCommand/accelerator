package ltd.inmind.user_service.model;

import com.baomidou.mybatisplus.annotation.IdType;
import com.baomidou.mybatisplus.annotation.TableId;
import com.baomidou.mybatisplus.annotation.TableName;

import java.util.Date;

@TableName("t_oauth2_access_token")
public class Oauth2AccessToken {

    @TableId(type = IdType.AUTO)
    private Integer id;

    private String accessToken;

    private Integer uId;

    private Integer clientPrimaryId;

    private Date expiredTime;

    private Date createTime;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public Integer getuId() {
        return uId;
    }

    public void setuId(Integer uId) {
        this.uId = uId;
    }

    public Integer getClientPrimaryId() {
        return clientPrimaryId;
    }

    public void setClientPrimaryId(Integer clientPrimaryId) {
        this.clientPrimaryId = clientPrimaryId;
    }

    public Date getExpiredTime() {
        return expiredTime;
    }

    public void setExpiredTime(Date expiredTime) {
        this.expiredTime = expiredTime;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
}
